package edu.techsiel1.commonTypes;

/**
 * Enum representing different roles that users can have in the system.
 * This enum defines the standard user roles: ROLE_READER and ROLE_ADMIN.
 */
public enum UserRole {
    /**
     * Represents a standard reader role, with limited access and privileges.
     */
    ROLE_READER,

    /**
     * Represents an administrator role, with full access and administrative privileges.
     */
    ROLE_ADMIN
}
